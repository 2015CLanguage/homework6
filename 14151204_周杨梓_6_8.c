#include<stdio.h>
int main()
{
	int a[50][50],l,c,i,j,temp,max[50],min[50],flag;
	printf("请输入要输入的数组的行数和列数:\n");
	scanf("%d%d",&l,&c);
	printf("请输入数组元素，从第一行输到最后一行，每次输入一个元素:\n");
	for(i=0;i<l;i++)
	{
		for(j=0;j<c;j++)
		{
			scanf("%d",&a[i][j]);
		}
	}
	printf("输入数组为:");
	for(i=0;i<l;i++)
	{
		printf("\n");
		for(j=0;j<c;j++)
		{
			printf("%d ",a[i][j]);
		}
	}
	printf("\n");
	for(i=0;i<l;i++)
	{
		max[i]=a[i][0];
	}
	for(j=0;j<c;j++)
	{
		min[j]=a[0][j];
	}
	for(i=0;i<l;i++)
	{
		for(j=0;j<c-1;j++)
		{
			if(max[i]<a[i][j+1])
			{
				max[i]=a[i][j+1];
			}
		}
	}
	for(j=0;j<c;j++)
	{
		for(i=0;i<l-1;i++)
		{
			if(min[j]>a[i+1][j])
			{
				min[j]=a[i+1][j];
			}
		}
	}
	flag=0;
	for(i=0;i<l;i++) 
	{
		for(j=0;j<c;j++)
		{
			if(min[j]==max[i])
			{
				printf("鞍点为%d,位于第%d行,第%d列",min[j],i+1,j+1);
				flag=1;
			}
		}
	}
	if(flag==0)
	{
		printf("不存在鞍点!");
		return 0;
	}
 } 
